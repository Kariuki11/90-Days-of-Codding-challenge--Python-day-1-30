# Introduction to Python

# Python is a high-level, interpreted programming language known for its readability and simplicity.
# It supports multiple programming paradigms, including procedural, object-oriented, and functional programming.

# Variables and Data Types

# Variables are used to store data values. In Python, you don't need to declare the type of a variable explicitly.
# The type is inferred from the value assigned to the variable.

# Example of variable assignment
x = 5          # Integer
y = 3.14       # Float
name = "Alice" # String
is_active = True # Boolean

# Data Types
# Python has several built-in data types, including:

# 1. Numbers
#    - Integers (int)
a = 10
b = -5

#    - Floating-point numbers (float)
pi = 3.14159
gravity = 9.8

#    - Complex numbers (complex)
c = 2 + 3j

# 2. Strings (str)
greeting = "Hello, World!"
multiline_string = """This is a
multiline string."""

# 3. Booleans (bool)
is_sunny = True
is_raining = False

# 4. Lists
# Lists are ordered, mutable collections of items.
fruits = ["apple", "banana", "cherry"]
numbers = [1, 2, 3, 4, 5]

# 5. Tuples
# Tuples are ordered, immutable collections of items.
coordinates = (10.0, 20.0)
colors = ("red", "green", "blue")

# 6. Dictionaries
# Dictionaries are unordered collections of key-value pairs.
person = {
    "name": "Alice",
    "age": 30,
    "city": "New York"
}

# 7. Sets
# Sets are unordered collections of unique items.
unique_numbers = {1, 2, 3, 4, 5}

# Basic Operations

# Arithmetic operations
sum_result = x + y
difference = x - y
product = x * y
quotient = x / y
remainder = x % y

# Comparison operations
is_equal = (x == y)
is_not_equal = (x != y)
is_greater = (x > y)
is_less = (x < y)

# Logical operations
and_result = (is_sunny and is_active)
or_result = (is_sunny or is_raining)
not_result = not is_sunny

# Conclusion
# This is a brief introduction to Python, covering variables, data types, and basic operations.
# Python is a versatile language with a rich set of libraries and frameworks, making it suitable for various applications.



